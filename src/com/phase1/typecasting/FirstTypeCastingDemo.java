package com.phase1.typecasting;

public class FirstTypeCastingDemo {

	public static void main(String[] args) {
		// IMplict type conversion
		
		int count = 100;
		
		long countBig = count;
		
		float countFloat = count;
		
		double countDouble = countBig;
		
		
		
		System.out.println("Int value" + count);
		System.out.println("Double value" + count);
		
		// Explicit type conversion
		
		
		
		

	}

}
