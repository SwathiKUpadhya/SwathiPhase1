package com.phase1.typecasting;

public class ExplicitTypeCastDemo {
	
	public static void main(String[] args) {
		// Explicit type conversion
		
	double cost = 100.45;
	
	long priceLong = (long)cost;
	
	int priceInt = (int) cost;
		
		
		System.out.println("Double to long" + priceLong);
		System.out.println("Double to int" + priceInt);
		

		
		
		
		

	}


}
